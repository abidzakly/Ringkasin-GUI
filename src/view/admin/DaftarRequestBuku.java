    private void accBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_accBtnActionPerformed
        int row = tabelRequest.getSelectedRow();
        try {
            int bookId = APP.getAllBuku().get(row).getBookId();
            if (APP.reviewBukuBaru(bookId)) {
                JOptionPane.showMessageDialog(
                        this,
                        "Berhasil menyimpan!",
                        "Alert Success",
                        1
                );
                APP.goToBookRequest(this);
            } else {
                JOptionPane.showMessageDialog(
                        this,
                        "Gagal menyimpan",
                        "Alert Failed",
                        1
                );
                APP.goToBookRequest(this);
            }
        } catch (SQLException ex) {
            System.out.println("Eksepsi: " + ex.getMessage());
            JOptionPane.showMessageDialog(null, "Exception: "
                    + ex.getMessage());
        }
    }//GEN-LAST:event_accBtnActionPerformed

