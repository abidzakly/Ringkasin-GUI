    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        int row = tabelRequest.getSelectedRow();
        try {
            int bookId = APP.getAllBuku().get(row).getBookId();
            int jawaban = JOptionPane.showConfirmDialog(this,
                    "Anda yakin ingin menghapus buku ini?", "Hapus Buku", JOptionPane.YES_NO_OPTION);
            if (jawaban == JOptionPane.YES_OPTION) {
                if (APP.hapusBuku(bookId)) {
                    JOptionPane.showMessageDialog(
                            this,
                            "Berhasil menghapus!",
                            "Alert Success",
                            1
                    );
                    APP.goToBookRequest(this);
                } else {
                    JOptionPane.showMessageDialog(
                            this,
                            "Gagal menghapus",
                            "Alert Failed",
                            1
                    );
                    APP.goToBookRequest(this);
                }
            }
        } catch (SQLException ex) {
            System.out.println("Eksepsi: " + ex.getMessage());
            JOptionPane.showMessageDialog(null, "Exception: "
                    + ex.getMessage());
        }
    }//GEN-LAST:event_deleteBtnActionPerformed

