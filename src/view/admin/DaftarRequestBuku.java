    private void populateTable() {
        model.setRowCount(0);
        ArrayList<Buku> daftarBuku;
        try {
            daftarBuku = APP.getAllBuku();
            for (Buku b : daftarBuku) {
                String status = "";
                switch (b.getStatus()) {
                    case 0 ->
                        status = "Menunggu disetujui";
                    case 1 ->
                        status = "Telah disetujui";
                    case 2 ->
                        status = "Telah diringkas user";
                }
                Object[] isiData = {b.getPemohon(), b.getTitle(), b.getAuthor(), b.getTotal_page(), b.getPub_year(), b.getIsbn(), status};
                model.addRow(isiData);
            }
        } catch (SQLException ex) {
            System.out.println("Eksepsi: " + ex.getMessage());
            JOptionPane.showMessageDialog(null, "Exception: "
                    + ex.getMessage());
        }

    }

