    private void populateTable() {
        model.setRowCount(0);
        ArrayList<Ringkasan> daftarRingkasan;
        try {
            daftarRingkasan = APP.getAllRingkasan();
            for (Ringkasan r : daftarRingkasan) {
                String status = "";
                switch (r.getStatus()) {
                    case 1 ->
                        status = "Menunggu direview";
                    case 2 ->
                        status = "Telah disetujui";
                    case 3 ->
                        status = "Ditolak";
                }
                Object[] isiData = {r.getPeringkas(), r.getTitle(), r.getGenre(), status};
                model.addRow(isiData);
            }
        } catch (SQLException ex) {
            System.out.println("Eksepsi: " + ex.getMessage());
            JOptionPane.showMessageDialog(null, "Exception: "
                    + ex.getMessage());
        }

    }

