    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        int row = tabelReview.getSelectedRow();
        try {
            int bookId = APP.getAllRingkasan().get(row).getBookId();
            int userId = APP.getAllRingkasan().get(row).getUserId();
            int jawaban = JOptionPane.showConfirmDialog(this,
                    "Anda yakin ingin menghapus ringkasan ini?", "Hapus Ringkasan", JOptionPane.YES_NO_OPTION);
            if (jawaban == JOptionPane.YES_OPTION) {
                if (APP.hapusRingkasan(bookId, userId)) {
                    JOptionPane.showMessageDialog(
                            this,
                            "Berhasil menghapus!",
                            "Alert Success",
                            1
                    );
                    APP.goToBookReview(this);
                } else {
                    JOptionPane.showMessageDialog(
                            this,
                            "Gagal menghapus",
                            "Alert Failed",
                            1
                    );
                    APP.goToBookReview(this);
                }
            }
        } catch (SQLException ex) {
            System.out.println("Eksepsi: " + ex.getMessage());
            JOptionPane.showMessageDialog(null, "Exception: "
                    + ex.getMessage());
        }
    }//GEN-LAST:event_deleteBtnActionPerformed

