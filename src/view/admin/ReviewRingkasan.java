    private void btnAcceptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAcceptActionPerformed
        int row = tabelReview.getSelectedRow();
        try {
            int bookId = APP.getAllRingkasan().get(row).getBookId();
            int userId = APP.getAllRingkasan().get(row).getUserId();
            if (APP.reviewRingkasan(btnAccept.getText(), bookId, userId)) {
                JOptionPane.showMessageDialog(
                        this,
                        "Berhasil menyimpan!",
                        "Alert Success",
                        1
                );
                APP.goToBookReview(this);
            } else {
                JOptionPane.showMessageDialog(
                        this,
                        "Gagal menyimpan",
                        "Alert Failed",
                        1
                );
                APP.goToBookReview(this);
            }
        } catch (SQLException ex) {
            System.out.println("Eksepsi: " + ex.getMessage());
            JOptionPane.showMessageDialog(null, "Exception: "
                    + ex.getMessage());
        }
    }//GEN-LAST:event_btnAcceptActionPerformed

