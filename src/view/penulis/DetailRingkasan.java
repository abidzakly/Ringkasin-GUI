    private void hapusRingkasanBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hapusRingkasanBtnActionPerformed
        int jawaban = JOptionPane.showConfirmDialog(this, "Anda yakin ingin menghapus Ringkasan anda?",
                "Hapus Ringkasan", JOptionPane.YES_NO_OPTION);
        if (jawaban == JOptionPane.YES_OPTION) {
            try {
                if (APP.hapusRingkasan(bookId, userId)) {
                    JOptionPane.showMessageDialog(null, "Berhasil Hapus");
                    APP.goToLibrary(this);
                } else {
                    APP.goToLibrary(this);
                }
            } catch (SQLException ex) {
                System.out.println("Eksepsi: " + ex.getMessage());
                JOptionPane.showMessageDialog(null, "Exception: "
                        + ex.getMessage());
            }
        }
    }//GEN-LAST:event_hapusRingkasanBtnActionPerformed

