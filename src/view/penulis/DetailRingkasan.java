    private void submitRingkasanBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitRingkasanBtnActionPerformed
        try {
            if (APP.insertRingkasan(userId, bookId, contentRingkasan)) {
                JOptionPane.showMessageDialog(
                        this,
                        "Berhasil menyimpan!",
                        "Alert Success",
                        1
                );
                APP.goToLibrary(this);
            } else {
                JOptionPane.showMessageDialog(
                        this,
                        "Gagal menyimpan",
                        "Alert Failed",
                        1
                );

            }
        } catch (SQLException ex) {
            System.out.println("Eksepsi: " + ex.getMessage());
            JOptionPane.showMessageDialog(null, "Exception: "
                    + ex.getMessage());
        }
    }//GEN-LAST:event_submitRingkasanBtnActionPerformed

